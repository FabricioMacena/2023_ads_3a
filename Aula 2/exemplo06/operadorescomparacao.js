// menor que (<)
console.log("2 < 2 = " + (2 < 2))

// maior que (>)
console.log("2 > 2 = " + (2 > 2))

// maior ou igual que (>=)
console.log("2 >= 2 = " + (2 >= 2))

// menor ou maior que (<=)
console.log("2 <= 2 = " + (2 <= 2))

// igual (==)
console.log("2 == 2 = " + (2 == 2))

// diferente ((!=)
console.log("2 != 2 = " + (2 != 2))

// identico (===)
console.log("2 === 2 = " + (2 === 2))