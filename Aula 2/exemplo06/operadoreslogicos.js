// && - and - "E" --> Só retornará true quando ambos forem verdade
console.log("Operador 'E': ")
console.log(true && true)
console.log(true && false)
console.log(false && false)
console.log("****************")

// || - or - "OU" -- > Só retornará true se alguma das condições serem verdade
console.log("Operador 'OU': ")
console.log(true || true)
console.log(true || false)
console.log(false || false)
console.log("****************")

// ! - not - "NEGAÇAO" --> inverte o estado lógico 
console.log("Inversão: ")
console.log(!true)
console.log(!false)